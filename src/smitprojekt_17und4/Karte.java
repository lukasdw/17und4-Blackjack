package smitprojekt_17und4;

public class Karte {

    private int wert;
    private String name;
    private String art;

    public int getWert() {
        return wert;
    }

    public void setWert(int wert) {
        this.wert = wert;
    } 

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getArt() {
        return art;
    }

    public void setArt(String art) {
        this.art = art;
    }
}